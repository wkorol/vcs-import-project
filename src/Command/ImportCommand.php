<?php
namespace App\Command;

use App\Repository\OrgRepository;
use App\Repository\RepoRepository;
use App\Services\BitbucketService;
use App\Services\GithubService;
use Symfony\Component\Console\Command\Command;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Output\OutputInterface;
use Symfony\Component\Console\Input\InputArgument;
use Symfony\Component\DependencyInjection\ParameterBag\ParameterBagInterface;
use Symfony\Component\Messenger\MessageBusInterface;

class ImportCommand extends Command {
    protected static $defaultName = 'import:repository';

    private $githubService;
    private $repoRepository;
    private $bitbucketService;
    private $providers;
    private $importCommandCreator;

    public function __construct(MessageBusInterface $bus , GithubService $githubService, RepoRepository $repoRepository, bitbucketService $bitbucketService, ParameterBagInterface $params)
    {
        $this->importCommandCreator = $bus;
        $this->params = $params;
        $this->repoRepository = $repoRepository;
        $this->githubService = $githubService;
        $this->bitbucketService = $bitbucketService;
        $this->providers = $params->get('providers');
        
        parent::__construct();
    }

    public function checkProviderExistance($providerName) : bool {
        if(in_array(strtolower($providerName), $this->providers)) {
            return true;
        }
        return false;
    }
    protected function importCommandCreator() {

    }

    protected function configure()
    {
        $this->addArgument('username', InputArgument::REQUIRED, 'Organisation Name');
        $this->addArgument('provider', InputArgument::REQUIRED, 'Provider Name');

        parent::configure(); // TODO: Change the autogenerated stub
    }
    protected function execute(InputInterface $input, OutputInterface $output): int
    {

        $orgName = $input->getArgument('username');
        $provider = $input->getArgument('provider');
        
        if($this->checkProviderExistance($provider) && !$this->repoRepository->findOrgWithProvider($provider, $orgName)) {
            switch(strtolower($provider)) {
                case 'github':
                    if(!$this->githubService->importToDb($orgName)) {
                        return COMMAND::FAILURE;
                    }
                    break;
                case 'bitbucket':
                    if(!$this->bitbucketService->importToDb($orgName)) {
                        return COMMAND::FAILURE;
                    }
                    break;
            }

            $importCommand = $this->importCommandCreator->create($input->getArgument('username'), $input->getArgument('provider'));
            $this->bus->dispatch($importCommand);


            $output->writeln('Successfully imported ' . $orgName . ' into the ' . $provider . ' repository.');
            return Command::SUCCESS;
        }
            $output->writeln($provider . ' is not implemented yet, or ' . $orgName . ' is already in DB with this provider');
            return Command::FAILURE;
        
    }
    

}